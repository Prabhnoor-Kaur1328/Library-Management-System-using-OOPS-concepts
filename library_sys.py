        print("5️⃣ Track borrowed books")
        print("6️⃣ Exit")

        choice = input("\nEnter your choice (1-6): ")

        if choice == "1":
            library.show_books()
        elif choice == "2":
            library.borrow_book()
        elif choice == "3":
            library.return_book()
        elif choice == "4":
            library.donate_book()
        elif choice == "5":
            library.track_borrowed_books()
